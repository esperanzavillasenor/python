"""
+	Addition 	x + y 	
- 	Subtraction 	x - y 	
* 	Multiplication 	x * y 	
/ 	Division 	x / y 	
% 	Modulus 	x % y 	
** 	Exponentiation 	x ** y 	 
// 	Floor division 	x // y 	 
"""
print ("33 + 17", 33 + 17)
print ("33 - 17", 33 - 17)
print ("33 * 17", 33 * 17)
print ("33 / 17", 33 / 17)
print ("33 % 17", 33 % 17)
print ("33 ** 17", 33 ** 17)
print ("37 // 17", 37 // 17)
